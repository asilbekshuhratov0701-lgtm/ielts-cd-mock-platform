import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const srcPath = path.join(root, "packages", "db", "prisma", "schema.prisma");
const outPath = path.join(root, "packages", "db", "prisma", "schema.sqlite.prisma");

let schema = readFileSync(srcPath, "utf8");

const enumNames = [...schema.matchAll(/^enum\s+(\w+)\s*\{/gm)].map((m) => m[1]);

schema = schema.replace(/^enum\s+\w+\s*\{[\s\S]*?\}\s*/gm, "");

for (const name of enumNames) {
  const fieldType = new RegExp(`^(\\s+[A-Za-z_]\\w*\\s+)${name}(\\??)(\\s|$)`, "gm");
  schema = schema.replace(fieldType, "$1String$2$3");
}

schema = schema.replace(/@default\(([A-Z][A-Z0-9_]*)\)/g, '@default("$1")');
schema = schema.replace(/\s*@db\.Text/g, "");

schema = schema.replace(
  /datasource\s+db\s*\{[\s\S]*?\}/m,
  'datasource db {\n  provider = "sqlite"\n  url      = env("DATABASE_URL")\n}'
);

const banner =
  "// AUTO-GENERATED for local development from schema.prisma — do not edit.\n" +
  "// Regenerated by scripts/gen-sqlite-schema.mjs (run via `pnpm db:local`).\n\n";

writeFileSync(outPath, banner + schema.trimStart());
console.log("[sqlite-schema] wrote", path.relative(root, outPath));
